        <h2>Что такое стереометрия?</h2>
        <p>Стереометрия — раздел евклидовой геометрии, в котором изучаются свойства фигур в пространстве. Основными (простейшими) фигурами в пространстве являются точки, прямые и плоскости. В стереометрии появляется новый вид взаимного расположения прямых: скрещивающиеся прямые. Это одно из немногих существенных отличий стереометрии от планиметрии, так как во многих случаях задачи по стереометрии решаются путём рассмотрения различных плоскостей, в которых выполняются планиметрические законы.

            <br><br>Не стоит путать этот раздел с планиметрией, поскольку в планиметрии изучаются свойства фигур на плоскости (свойства плоских фигур), а в стереометрии — свойства фигур в пространстве (свойства пространственных фигур).</p>
